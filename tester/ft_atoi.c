int	ft_atoi(const char *str)
{
	int h;
	int c;
	int i;

	h = 0;
	i = 0;
	c = 1;
	while (str[i] && str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			c *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		h = h * 10 + (str[i] - '0');
		i++;
	} 
	return ((int)h * c);
}
