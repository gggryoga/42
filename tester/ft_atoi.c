int	ft_atoi(const char *str)
{
	unsigned int h;
	int c;
	int i;

	h = 0;
	i = 0;
	c = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\f' || str[i] == '\r' || str[i] == '\v')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			c = -1;
		i++;
	}
	while ('0' <= str[i] && str[i] <= '9')
	{
		h = h * 10 + (str[i] - '0');
		i++;
	}
	return ((int)c * h);
}
